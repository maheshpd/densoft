package com.densoftinfotech.densoftpaysmart.classes;

public class LeaveAppliedDetails {
    private String P_Id = "";
    private String StaffId = "";
    private String Name = "";
    private String FromDate = "";
    private String ToDate = "";
    private String NoOfDays = "";
    private String AccFromDate = "";
    private String AccToDate = "";
    private String AccNoOfDays = "";
    private String Status = "";

    public String getP_Id() {
        return P_Id;
    }

    public void setP_Id(String p_Id) {
        P_Id = p_Id;
    }

    public String getStaffId() {
        return StaffId;
    }

    public void setStaffId(String staffId) {
        StaffId = staffId;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getFromDate() {
        return FromDate;
    }

    public void setFromDate(String fromDate) {
        FromDate = fromDate;
    }

    public String getToDate() {
        return ToDate;
    }

    public void setToDate(String toDate) {
        ToDate = toDate;
    }

    public String getNoOfDays() {
        return NoOfDays;
    }

    public void setNoOfDays(String noOfDays) {
        NoOfDays = noOfDays;
    }

    public String getAccFromDate() {
        return AccFromDate;
    }

    public void setAccFromDate(String accFromDate) {
        AccFromDate = accFromDate;
    }

    public String getAccToDate() {
        return AccToDate;
    }

    public void setAccToDate(String accToDate) {
        AccToDate = accToDate;
    }

    public String getAccNoOfDays() {
        return AccNoOfDays;
    }

    public void setAccNoOfDays(String accNoOfDays) {
        AccNoOfDays = accNoOfDays;
    }

    public String getStatus() {
        return Status;
    }

    public void setStatus(String status) {
        Status = status;
    }
}
